    /**
     * Initialisation
     */
    private void init() {
        // TODO add your handling code here:
        ConnectionBDD connectionBDD = new ConnectionBDD();

        // mapping de la connexion de l'annuaire "this.connection"  avec la connexion retournée par 
        // connectionBDD.openConnectionBDD()
        this.connection = connectionBDD.openConnectionBDD();

        // Création (instantiation) d'un DAO de l'arrondissement en utilisant le
        // constructeur de la classe ArrondissementDAO qui prend en paramètre 
        // la connexion de l'annuaire "this.connection" 
        ArrondissementDAO arrondissementDAO = new ArrondissementDAO(this.connection);

        // récupération de la liste des arrondissement mappée avec la liste "listeArrondissement" 
        // en executant la requête "select_all" définie dans la méthode findAllArrondissement() du DAO
        List<Arrondissement> listeArrondissement = arrondissementDAO.findAllArrondissement();

        comboBoxArrondissement.addItem("");

        // boucle sur la liste des arrondissements et ajout d'un item dans la combo box de
        // l'arrondissement courant
        for (Arrondissement arrond : listeArrondissement) {
            comboBoxArrondissement.addItem(arrond.getNomArrondissement());
        }
    }


