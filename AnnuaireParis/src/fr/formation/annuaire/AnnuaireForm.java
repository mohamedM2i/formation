    private void comboBoxArrondissementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxArrondissementActionPerformed

        String nomArrondissementSelected = comboBoxArrondissement.getSelectedItem().toString();

        ArrondissementDAO arrondissementDAO = new ArrondissementDAO(this.connection);

        int idArrondissementSelected = arrondissementDAO.getIdFromNomArrondissement(nomArrondissementSelected);

        ContacteDAO contacteDAO = new ContacteDAO(this.connection);

        List<Contacte> listeContacteSelected = contacteDAO.findByArrondissement(idArrondissementSelected);

        String affichage = "";
        for (Contacte contacteSelected : listeContacteSelected) {
            affichage+= contacteSelected.toString();
        }
        
        textPaneContact.setText(affichage);
        
        
    }//GEN-LAST:event_comboBoxArrondissementActionPerformed

